console.log(numero1, " + ", numero2, " = ", numero1 + numero2)

console.log(".... Subtração....")
console.log(numero1, " - ", numero2, " = ", numero1 - numero2)

console.log(".... Multiplicação....")
console.log(numero1, " * ", numero2, " = ", numero1 * numero2)

console.log(".... Divisão....")
console.log(numero1, " / ", numero2, " = ", numero1 / numero2)

console.log(".... Potencia....")
console.log(numero1, " ** ", numero2, " = ", numero1 ** numero2)

console.log(".... Porcentagem....")
